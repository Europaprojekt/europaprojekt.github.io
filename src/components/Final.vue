            <h1>Die AfD - Der Rechtsextreme Verdachtsfall</h1>
            <p>
                Die AfD erhält immer mehr Stimmen in den Wahlen. Das liegt daran, dass der Rechts&shy;radikalismus zunimmt, weil viele Menschen denken, dass die AfD die richtige Lösung für die heutige Gesell&shy;schaft ist. Doch was sind die Ziele der AfD und wie könnte eine Zukunft mit der AfD in der Regierung aussehen und warum sollte man sie nicht wählen?<br>
                <br>
